<h3>AMToolbox Notes</h3>
<p>The AMToolbox notes is a collection of information relating to AMToolbox.
An AMToolbox note may be everything from a ph.d. thesis, a preprint of a journal paper to a note
describing the design of a function etc.</p>
<p>Contributions are welcome, see <a href="http://ltfat.sourceforge.net/notes/ltfatnote001.pdf">the LTFAT documentation</a>
for details on the format.</p>
