        <p>This is the homepage for the  Pitu Danish nonsense syllable project.</p>

        <h3>Introduction to the speech material</h3>

        <p>The material consists of recordings of nonsense syllables spoken by 27 native talkers of Danish made in an anechoic chamber.
        17 Danish consonants were used [ptkbdgfsvmnrlhjSW]  (notation described in (Henrichsen, 2007),  special version of SAMPA).
        One of three vowels [iau]  followed the initial consonant (vowel [a] = [z] in the notation of Henrichsen (2007)).
        A second syllable [tu] followed the CV-syllables in order to ensure that no token was "accidentally" a Danish word.
        Each syllable was repeated three times following the standard intonation pattern for Danish (falling F0).
        Please refer to the documentation for further details.</p>

        <p>An introduction to the speech corpus can be found in this
        <a href="http://server.elektro.dtu.dk/ftp/tuc/Pitu/Backgrnd.pdf">pdf</a> file.</p>


